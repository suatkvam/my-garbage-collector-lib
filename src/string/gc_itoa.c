#include "internal_collector.h"

static int	gc_numlen(long n)
{
	int	len;

	len = 0;
	if (n <= 0)
		len = 1;
	while (n)
	{
		len++;
		n /= 10;
	}
	return (len);
}

char	*gc_itoa(t_gc_context *contex, int n)
{
	char	*str;
	long	nb;
	int		len;

	if (!contex)
		return (NULL);
	nb = n;
	len = gc_numlen(nb);
	str = (char *)gc_malloc(contex, len + 1);
	if (!str)
		return (NULL);
	str[len] = '\0';
	if (nb < 0)
	{
		str[0] = '-';
		nb = -nb;
	}
	while (len-- > (n < 0))
	{
		str[len] = (nb % 10) + '0';
		nb /= 10;
	}
	return (str);
}
